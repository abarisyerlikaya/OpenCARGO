    private void tableUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tableUpdateActionPerformed
        tableMessage.setText("");
        
        DefaultTableModel model = (DefaultTableModel)cargoTable.getModel();
        int row = cargoTable.getSelectedRow();
        if(row == -1){
            if(cargoTable.getRowCount() == 0){
                tableMessage.setText("Table is Empty");
            }
            else{
                tableMessage.setText("Please Select a Row");
            }
        }
        else{
            model.setValueAt(tableSName.getText(), row, 1);
            model.setValueAt(tableSNumber.getText(), row, 2);
            model.setValueAt(tableSAddress.getText(), row, 3);
            model.setValueAt(tableRName.getText(), row, 4);
            model.setValueAt(tableRNumber.getText(), row, 5);
            model.setValueAt(tableAddressDelivered.getText(), row, 6);
            model.setValueAt(state.getText(), row, 8);
            if(tableFragile.isSelected())
                model.setValueAt("True", row, 7);
            else
                model.setValueAt("False", row, 7);
            
            tableMessage.setText("Succesfully Updated");
            String str;
            if(tableFragile.isSelected()){
                str = "true";
            }
            else{
                str = "false";
            }
            
            db.updateCargo(model.getValueAt(row, 0).toString(),tableSName.getText(),
            tableSNumber.getText(),tableSAddress.getText(),tableRName.getText(),
            tableRNumber.getText(),tableAddressDelivered.getText(),str,state.getText());
        }
    }//GEN-LAST:event_tableUpdateActionPerformed
    
