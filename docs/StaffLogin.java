    private void cargoTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cargoTableMouseClicked
        DefaultTableModel model = (DefaultTableModel) cargoTable.getModel();
        int row = cargoTable.getSelectedRow();
        boolean b;
        if(model.getValueAt(row, 7).toString().equals("true")){
            b = true;
        }
        else{
            b = false;
        }
        
        tableSName.setText(model.getValueAt(row, 1).toString());
        tableSNumber.setText(model.getValueAt(row, 2).toString());
        tableSAddress.setText(model.getValueAt(row, 3).toString());
        tableRName.setText(model.getValueAt(row, 4).toString());
        tableRNumber.setText(model.getValueAt(row, 5).toString());
        tableAddressDelivered.setText(model.getValueAt(row, 6).toString()); 
        tableFragile.setSelected(b);
        
    }//GEN-LAST:event_cargoTableMouseClicked

    private void tableArrivedActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tableArrivedActionPerformed
        DefaultTableModel model = (DefaultTableModel) cargoTable.getModel();
        int row = cargoTable.getSelectedRow();
        if(row == -1){
            if(cargoTable.getRowCount() == 0){
                tableMessage.setText("Table is Empty");
            }
            else{
                tableMessage.setText("Please Select a Row");
            } 
        }
        else{
            db.packageArrived(model.getValueAt(row, 0).toString());
            model.removeRow(row);
        }
    }//GEN-LAST:event_tableArrivedActionPerformed

